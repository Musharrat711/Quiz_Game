class Quiz:
    current_List = []
    current_answers = []

    total_score = 0
    round = 0

    @staticmethod
    def __display_intro():
        print("                           Welcome to the MJ Quiz Game                                  ")
        print("You have to choose from given Genres for Quiz")
        print("Each genre has 10 questions")

    @staticmethod
    def __choose_genre():
        pf_genre = int(input("\n1. General Knowledge\n"
                             "2. Music\n"
                             "3. Movies\n"
                             "4. Animals and Birds\n"
                             "5. Programming\nGive answer in num: \n"))
        return pf_genre

    def __assign_List(self, genre):
        quest = Questions()
        if genre == 1:
            self.current_List = quest.GK_Questions
            self.current_answers = quest.gk_answers
        elif genre == 2:
            self.current_List = quest.Music
            self.current_answers = quest.mk_ans
        elif genre == 3:
            self.current_List = quest.Movies
            self.current_answers = quest.mov_ans
        elif genre == 4:
            self.current_List = quest.Animals
            self.current_answers = quest.ani_ans
        elif genre == 5:
            self.current_List = quest.Programming
            self.current_answers = quest.prog_ans

    def start_game(self):
        self.total_score = 0
        if self.round == 0:
            self.__display_intro()
            self.round += 1
        genre = self.__choose_genre()
        self.__assign_List(genre)
        self.__display_next_question(0)

    def __continue_game(self):
        response = input("Do You want to continue playing this game!: ")
        if response in ('y', "Y", 'yes', 'YES', "Yes"):
            self.start_game()
        else:
            print("Thank You for Playing!\nHave a nice day\nKeep Learning\nKeep Improving :)")

    def __show_result(self, num):
        answer = input("Choose option: ")
        if answer == self.current_answers[num][0]:
            print("Congratulation! You got the right answer\n")
            self.total_score += 1
        else:
            print(":( Your answer is Wrong")
            print(f"The correct answer is : {self.current_answers[num]}\n")

        if num < 9:
            self.__display_next_question(num + 1)
        else:
            print("\nYour total score is: ", self.total_score)
            self.__continue_game()

    def __display_next_question(self, num):
        print(f"{num + 1}: {self.current_List[num]}")
        self.__show_result(num)


def main():
    my_quiz = Quiz()
    my_quiz.start_game()


main()
